#include<iostream>
using namespace std;
/////////////////////////////////////////////////////////////////////////////
//  Function name:  Display
//  Description:    It use to Display 1 to 5 using recurrsion
//  Author:         Atharva Sanjay Dhumal.
//  Date:           15/12/2025
//
/////////////////////////////////////////////////////////////////////////////
void Display()
{
    static int iCount = 1;

    if(iCount <= 5)
    {
        cout<<(iCount)<<"\t";
        iCount++;
        Display();
    }

}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();


    return 0;
}

