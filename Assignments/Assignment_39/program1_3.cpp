#include<iostream>
using namespace std;
/////////////////////////////////////////////////////////////////////////////
//  Function name:  Display
//  Description:    It use to Display 5 to 1 numbers using recurrsion
//  Author:         Atharva Sanjay Dhumal.
//  Date:           15/12/2025
//
/////////////////////////////////////////////////////////////////////////////
void Display()
{
    static int iCount = 5;

    if(iCount >= 1)
    {
        cout<<(iCount)<<"\t";
        iCount--;
        Display();
    }

}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();


    return 0;
}

