#include<iostream>
using namespace std;
/////////////////////////////////////////////////////////////////////////////
//  Function name:  Display
//  Description:    It use to Display A to F alphabets using recurrsion
//  Author:         Atharva Sanjay Dhumal.
//  Date:           15/12/2025
//
/////////////////////////////////////////////////////////////////////////////
void Display()
{
    static char ch = 'A';

    if(ch <= 'F')
    {
        cout<<(ch)<<"\t";
        ch++;
        Display();
    }

}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();


    return 0;
}

