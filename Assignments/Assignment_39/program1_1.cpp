/////////////////////////////////////////////////////////////////////////////
void Display()
{
    static int iCount = 0;

    if(iCount <= 4)
    {
        cout<<("*\t");
        iCount++;
        Display();
    }

}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();


    return 0;
}

