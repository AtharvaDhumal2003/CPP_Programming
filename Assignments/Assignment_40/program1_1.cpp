void Display(int iNo)
{
    static int iCount = 1;

    if(iCount <= iNo)
    {
        cout<<("*")<<"\t";
        iCount++;
        Display(iNo);
    }

}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue  = 0;

    cout<<("Enter number:")<<"\n";
    cin>>(iValue);

    Display(iValue);

    return 0;
}
