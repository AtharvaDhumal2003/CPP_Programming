#include<iostream>
using namespace std;
/////////////////////////////////////////////////////////////////////////////
//  Function name:  Display
//  Description:   It use to Display numbers from 1 till given input
//                  number using recurrsion
//  Author:         Atharva Sanjay Dhumal.
//  Date:           15/12/2025
//
/////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    static int iCount = 1;

    if(iCount <= iNo)
    {
        cout<<(iCount)<<"\t";
        iCount++;
        Display(iNo);
    }

}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue  = 0;

    cout<<("Enter number:")<<"\n";
    cin>>(iValue);

    Display(iValue);

    return 0;
}
