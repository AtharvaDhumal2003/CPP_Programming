using namespace std;
/////////////////////////////////////////////////////////////////////////////
//  Function name:  Factorial
//  Description:    It use to calculate factorial of input number
//                  using recurrsion
//  Input:          int
//  Output:         int
//  Author:         Atharva Sanjay Dhumal.
//  Date:           15/12/2025
//
/////////////////////////////////////////////////////////////////////////////

int Factorial(int iNo)
{
    static int iFatc = 1;
    if(iNo >= 1)
    {
        iFatc = iFatc * iNo;
        iNo--;
        Factorial(iNo);
    }

    return iFatc;

}
/////////////////////////////////////////////////////////////////////////////
//  
//  Entry Point Function for the application
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue  = 0;
    int iRet = 0;

    cout<<("Enter number:");
    cin>>(iValue);

    iRet = Factorial(iValue);

    cout<<"Factorial of number is:"<<iRet<<"\n";

    return 0;
}
